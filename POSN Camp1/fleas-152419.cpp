#include<stdio.h>
int n,c,c2,idx,l,r,idxl,idxr,rem,ok,idxu;
int u;
char s[300][300];
main()
{
	scanf("%d",&n);
	for(c=0;c<300;c++)
	{
		for(c2=0;c2<300;c2++)
		{
			s[c][c2]='.';
		}
	}
	for(c=1;c<=n;c++)
	{
		for(c2=1;c2<=c;c2++)
		{
			if(c==1&&rem<n)
			{
				s[c][133]=idx;
				idx++;
				rem++;
				idx%=26;
				if(rem==n)
				{
					break;
				}
				continue;
			}
			else if(c%2==1&&rem<n)
			{
				s[c][133+c2-1]=idx;
				idx++;
				rem++;
				idx%=26;
				if(rem==n)
				{
					break;
				}
			}
			else if(c%2==0&&rem<n)
			{
				s[c][133-c2+1]=idx;
				idx++;
				rem++;
				idx%=26;
				if(rem==n)
				{
					break;
				}
			}
		}
		if(ok==1)
		{
			break;
		}	
	}
	for(c=0;c<=133;c++)
	{
		for(c2=0;c2<300;c2++)
		{	
			if(s[c2][c]!='.')
			{
				l=c;
				idxl=1;
				break;
			}
		}
		if(idxl==1)
		{
			break;
		}
	}
	for(c=299;c>=133;c--)
	{
		for(c2=0;c2<300;c2++)
		{	
			if(s[c2][c]!='.')
			{
				r=c;
				idxr=1;
				break;
			}
		}
		if(idxr==1)
		{
			break;
		}
	}
	for(c=299;c>=1;c--)
	{
		for(c2=0;c2<300;c2++)
		{	
			if(s[c][c2]!='.')
			{
				u=c;
				idxu=1;
				break;
			}
		}
		if(idxu==1)
		{
			break;
		}
	}
//	printf("%d\n%d\n",l,r);
	for(c=1;c<=u;c++)
	{
		for(c2=l;c2<=r;c2++)
		{
			if(s[c][c2]!='.')
			{
				printf("%c",s[c][c2]+'A');
			}
			else 
			{
				printf(".");
			}
		}
		printf("\n");
	}
	
	
	
}
