#include<stdio.h>
#include<string.h>
char n,s[10][1000],k[6][300],cc;
int c,c2,rem[10],len[10],mc,reml,remr,idxl,idxr;
main()
{
	scanf("%c",&n);
	for(c=0;c<6;c++)
	{
		scanf("%s",s[c]);
		len[c]=strlen(s[c]);
		//printf("%d\n",len[c]);
		if(len[c]>mc)
		{
			mc=len[c];
		}
	}
	for(c=0;c<6;c++)
	{
		for(c2=0;c2<300;c2++)
		{
			k[c][c2]='.';
			//printf("%c",k[c][c2]);
		}
		//printf("\n");
	}
	for(c=0;c<6;c++)
	{
		for(c2=0;c2<300;c2++)
		{
			if(s[c][c2]==n)
			{
				rem[c]=c2;
				//printf("%d ",rem[c]);
			}
		}
	}
	for(c=0;c<6;c++)
	{
		for(cc=0;cc<rem[c];cc++)
		{
			k[c][150-rem[c]+cc]=s[c][cc];
		}
		k[c][150]=n;
		for(cc=1;cc<len[c];cc++)
		{
			if(s[c][cc+rem[c]]!='\0')
			k[c][150+cc]=s[c][cc+rem[c]];
		}
	}
	for(c2=0;c2<200;c2++)
	{
		for(c=0;c<6;c++)
		{
			if(k[c][c2]!='.')
			{
			reml=c2;
			idxl=1;
			break;
			}
		}
		if(idxl==1)
		{
			break;
		}
	}
	for(c2=300-1;c2>=100;c2--)
	{
		for(c=0;c<6;c++)
		{
			if(k[c][c2]!='.')
			{
			remr=c2;
			idxr=1;
			break;
			}
		}
		if(idxr==1)
		{
			break;
		}
	}
	//printf("%d %d\n",remr,reml);
	for(c=0;c<6;c++)
	{
		for(c2=reml;c2<=remr;c2++)
		{
			printf("%c",k[c][c2]);
		}
		printf("\n");
	}
}
