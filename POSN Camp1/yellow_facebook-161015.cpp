#include<stdio.h>
#include<string.h>
int lens,len,c,c2,rem,idx,n,mc;
char s[56],m[66],a[56],ans[56];
main()
{
	scanf("%d",&n);
	scanf("%s",s);
	lens=strlen(s);
	mc=lens;
	for(c=0;c<lens;c++)
	{
		s[c]-='0';
	}
	for(c=0;c<lens/2;c++)
	{
		rem=s[c];
		s[c]=s[lens-c-1];
		s[lens-c-1]=rem;
	}
	for(c=0;c<n;c++)
	{
		for(c2=0;c2<66;c2++)
		{
			m[c2]=0;
			ans[c2]=0;
		}
		scanf("%s",a);
		len=strlen(a);
		for(c2=0;c2<len;c2++)
		{
			rem=a[c2];
			m[idx]=rem/10;
			idx++;
			rem=a[c2]%10;
			m[idx]=rem;
			idx++;
		}
		if(mc<len*2)
		{
			mc=len*2;
		}
		for(c2=0;c2<idx/2;c2++)
		{
			rem=m[c2];
			m[c2]=m[idx-c2-1];
			m[idx-c2-1]=rem;
		}
		/*/
		printf("\n");
		for(c2=0;c2<idx;c2++)
		{
			printf("%d",m[c2]);
		}/*/
		for(c2=0;c2<mc;c2++)
		{
			ans[c2]+=m[c2]+s[c2];
			if(ans[c2]>=10)
			{
				ans[c2+1]++;
				ans[c2]-=10;
			}
		}
		//*/
		if(ans[mc]!=0)
		{
			printf("%d",ans[lens]);
		}//*/
		for(c2=mc-1;c2>=0;c2--)
		{
			
			printf("%d",ans[c2]);
		}
		printf("\n");
		idx=0;
		mc=lens;
	}
}
